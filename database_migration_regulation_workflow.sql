    admission_number VARCHAR(50) NOT NULL,
    full_name VARCHAR(255) NOT NULL,
    program VARCHAR(255) NULL,
    intake_year YEAR NOT NULL,
    intake_semester ENUM('semester_1', 'semester_2', 'semester_3') NOT NULL,
    forwarded_by_user_id BIGINT UNSIGNED NULL,
    forwarded_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    confirmation_status ENUM('pending', 'confirmed') NOT NULL DEFAULT 'confirmed',
    notes TEXT NULL,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT fk_bursary_forward_user
        FOREIGN KEY (user_id) REFERENCES users(user_id)
        ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT fk_bursary_forwarded_by
        FOREIGN KEY (forwarded_by_user_id) REFERENCES users(user_id)
        ON DELETE SET NULL ON UPDATE CASCADE,
