-- ============================================================================
-- STEP 3: CREATE NEW DOCUMENT_SUBMISSIONS TABLE
-- (Replaces payment_submissions - now handles academic documents)
-- ============================================================================

CREATE TABLE document_submissions (
    submission_id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    user_id BIGINT UNSIGNED NOT NULL,
    
    -- Admission and Sponsorship
    admission_letter_path VARCHAR(500) NULL COMMENT 'Admission letter',
    is_bursary BOOLEAN DEFAULT FALSE COMMENT 'Whether student is bursary sponsored',
    bursary_award_letter_path VARCHAR(500) NULL COMMENT 'Bursary award letter (required when bursary)',

    -- Academic Documents (Required by regulation)
    s6_certificate_path VARCHAR(500) NULL COMMENT 'S.6 Certificate/Transcript',
    national_id_path VARCHAR(500) NULL COMMENT 'National ID copy',
    school_id_path VARCHAR(500) NULL COMMENT 'Former School ID copy',
    passport_photo_path VARCHAR(500) NULL COMMENT 'Passport-size photo',
    
    -- Student Information
    full_name VARCHAR(255) NOT NULL,
    date_of_birth DATE NOT NULL,
    program VARCHAR(255) NOT NULL,
    faculty VARCHAR(255) NOT NULL,
    intake_year YEAR NOT NULL,
    intake_semester ENUM('semester_1', 'semester_2', 'semester_3') NOT NULL,
    
    -- Payment Information (for finance verification)
    payment_reference VARCHAR(100) NULL COMMENT 'Bank reference number',
    payment_amount DECIMAL(10, 2) NULL COMMENT 'Amount paid',

    payment_currency ENUM('UGX','USD') NOT NULL DEFAULT 'UGX' COMMENT 'Currency of payment amount',
    payment_date DATE NULL COMMENT 'Date of payment',
    bank_slip_path VARCHAR(500) NULL COMMENT 'Bank slip proof',
    
    -- Registration Number (Generated by Admissions)
    registration_number VARCHAR(50) NULL UNIQUE COMMENT 'Auto-generated by admissions',
    
    -- Workflow Status
    status ENUM(
        'pending_admissions',           -- Step 1: Waiting for admissions review
        'under_admissions_review',      -- Step 2: Admissions reviewing documents
        'admissions_approved',          -- Step 3: Admissions approved, forwarded to finance
        'admissions_rejected',          -- Rejected by admissions
        'pending_finance',              -- Step 4: Waiting for finance payment confirmation
        'under_finance_review',         -- Step 5: Finance reviewing payment
        'finance_approved',             -- Step 6: Finance approved, back to admissions
        'finance_rejected',             -- Rejected by finance
        'pending_greencard',            -- Step 7: Waiting for green card generation
        'greencard_issued',             -- Step 8: Green card issued - COMPLETE
        'cancelled'                     -- Cancelled by student or admin
    ) NOT NULL DEFAULT 'pending_admissions',
    
    -- Tracking
    submitted_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    last_updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    
    -- Metadata
    priority_level ENUM('normal', 'urgent', 'very_urgent') DEFAULT 'normal',
    notes TEXT NULL COMMENT 'Internal notes',
    
    CONSTRAINT fk_doc_submission_user 
        FOREIGN KEY (user_id) REFERENCES users(user_id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    
    INDEX idx_user_submissions (user_id, submitted_at DESC),
    INDEX idx_status (status, submitted_at),
    INDEX idx_registration_number (registration_number)
) ENGINE=InnoDB COMMENT='Student document submissions following regulation workflow';

-- ============================================================================
-- STEP 4: CREATE ADMISSIONS_VERIFICATIONS TABLE
-- (New table - handles document verification by admissions office)
-- ============================================================================

CREATE TABLE admissions_verifications (
    verification_id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    submission_id BIGINT UNSIGNED NOT NULL UNIQUE,
    verified_by_user_id BIGINT UNSIGNED NOT NULL COMMENT 'Admissions staff member',
    
    -- Document Verification
    s6_certificate_verified BOOLEAN DEFAULT FALSE,
    national_id_verified BOOLEAN DEFAULT FALSE,
    school_id_verified BOOLEAN DEFAULT FALSE,
    documents_authentic BOOLEAN DEFAULT FALSE,
    
    -- Decision
    is_approved BOOLEAN NOT NULL,
    rejection_reason TEXT NULL COMMENT 'Required if rejected',
    verification_notes TEXT NULL COMMENT 'Admissions officer notes',
    
    -- Registration Number Generation
    registration_number VARCHAR(50) NULL COMMENT 'Generated upon approval',
    registration_generated_at TIMESTAMP NULL,
    
    -- Forwarding to Finance
    forwarded_to_finance BOOLEAN DEFAULT FALSE,
    forwarded_at TIMESTAMP NULL,
    
    -- Timing
    verification_duration_seconds INT UNSIGNED NULL,
    verified_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT fk_adm_verification_submission 
        FOREIGN KEY (submission_id) REFERENCES document_submissions(submission_id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    
    CONSTRAINT fk_adm_verification_user 
        FOREIGN KEY (verified_by_user_id) REFERENCES users(user_id)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    
    INDEX idx_verified_by (verified_by_user_id),
    INDEX idx_approved (is_approved, verified_at)
) ENGINE=InnoDB COMMENT='Admissions office document verification records';

-- ============================================================================
-- STEP 5: CREATE FINANCE_CLEARANCES TABLE
-- (Replaces payment_verifications - now handles payment clearance confirmation)
-- ============================================================================

CREATE TABLE finance_clearances (
    clearance_id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    submission_id BIGINT UNSIGNED NOT NULL UNIQUE,
    verified_by_user_id BIGINT UNSIGNED NOT NULL COMMENT 'Finance officer',
    
    -- Payment Verification
    payment_reference VARCHAR(100) NULL,
    payment_amount_verified DECIMAL(10, 2) NOT NULL,
    payment_date_verified DATE NOT NULL,
    required_amount DECIMAL(10, 2) NOT NULL,
    percentage_paid DECIMAL(5, 2) GENERATED ALWAYS AS ((payment_amount_verified / required_amount) * 100) STORED,
    
    -- Finance System Integration
    finance_api_reference VARCHAR(255) NULL COMMENT 'External finance system reference',
    finance_api_response JSON NULL COMMENT 'API response for audit',
    
    -- Decision
    is_cleared BOOLEAN NOT NULL COMMENT 'TRUE = payment confirmed, FALSE = rejected',
    clearance_notes TEXT NULL COMMENT 'Finance officer notes',
    rejection_reason TEXT NULL COMMENT 'Required if not cleared',
    
    -- Manual Handling
    manual_override BOOLEAN DEFAULT FALSE,
    override_reason TEXT NULL,
    
    -- Forwarding Back to Admissions
    forwarded_to_admissions BOOLEAN DEFAULT FALSE,
    forwarded_at TIMESTAMP NULL,
    
    -- Timing
    verification_duration_seconds INT UNSIGNED NULL,
    verified_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT fk_fin_clearance_submission 
        FOREIGN KEY (submission_id) REFERENCES document_submissions(submission_id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    
    CONSTRAINT fk_fin_clearance_user 
        FOREIGN KEY (verified_by_user_id) REFERENCES users(user_id)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    
    INDEX idx_verified_by (verified_by_user_id),
    INDEX idx_cleared (is_cleared, verified_at)
) ENGINE=InnoDB COMMENT='Finance department payment clearance records';

-- ============================================================================
-- STEP 6: RECREATE GREEN_CARDS TABLE (with updated references)
-- ============================================================================

CREATE TABLE green_cards (
    card_id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    submission_id BIGINT UNSIGNED NOT NULL UNIQUE,
    user_id BIGINT UNSIGNED NOT NULL,
    
    -- Card Details
    registration_number VARCHAR(50) NOT NULL UNIQUE,
    card_number VARCHAR(50) NOT NULL UNIQUE COMMENT 'Green card unique number',
    qr_code_data TEXT NOT NULL COMMENT 'QR code content',
    qr_code_image_path VARCHAR(500) NULL COMMENT 'Generated QR code image',
    
    -- Student Information
    full_name VARCHAR(255) NOT NULL,
    program VARCHAR(255) NOT NULL,
    faculty VARCHAR(255) NOT NULL,
    student_photo_path VARCHAR(500) NOT NULL,
    
    -- Validity
    issue_date DATE NOT NULL,
    expiry_date DATE NOT NULL,
    academic_year VARCHAR(20) NOT NULL,
    semester ENUM('semester_1', 'semester_2', 'semester_3') NOT NULL,
    
    -- PDF Document
    pdf_path VARCHAR(500) NOT NULL COMMENT 'Generated green card PDF',
    digital_signature TEXT NULL COMMENT 'Digital signature for authenticity',
    
    -- Status
    is_active BOOLEAN DEFAULT TRUE,
    revoked_at TIMESTAMP NULL,
    revocation_reason TEXT NULL,
    
    -- Issuance Details
    issued_by_user_id BIGINT UNSIGNED NOT NULL COMMENT 'Admissions officer who issued',
    issued_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    downloaded_at TIMESTAMP NULL COMMENT 'First download timestamp',
    download_count INT UNSIGNED DEFAULT 0,
    
    -- Metadata
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    
    CONSTRAINT fk_greencard_submission 
        FOREIGN KEY (submission_id) REFERENCES document_submissions(submission_id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    
    CONSTRAINT fk_greencard_user 
        FOREIGN KEY (user_id) REFERENCES users(user_id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    
    CONSTRAINT fk_greencard_issued_by 
        FOREIGN KEY (issued_by_user_id) REFERENCES users(user_id)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    
    INDEX idx_registration_number (registration_number),
    INDEX idx_user_cards (user_id, issue_date DESC),
    INDEX idx_active_cards (is_active, issue_date)
) ENGINE=InnoDB COMMENT='Digital green cards issued to students';

