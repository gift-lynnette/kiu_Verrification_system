    changed_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT fk_workflow_submission 
        FOREIGN KEY (submission_id) REFERENCES document_submissions(submission_id)
        ON DELETE CASCADE ON UPDATE CASCADE,
    
    CONSTRAINT fk_workflow_user 
        FOREIGN KEY (changed_by_user_id) REFERENCES users(user_id)
        ON DELETE SET NULL ON UPDATE CASCADE,
    
    INDEX idx_submission_history (submission_id, changed_at)
) ENGINE=InnoDB COMMENT='Complete audit trail of workflow status changes';

